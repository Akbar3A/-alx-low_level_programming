#include <stdio.h>
/**
 * main - print if the number is postive, zero, or negative
 *
 * Description: using the main function
 * The program prints "all single digit numbers of base 10 starting from 0, followed by a newline
 * Return: 0
 */
int main(void)
{
int r;
for (r = 0; r < 10; r++)
{
	printf("%i", r);
}
printf("\n");
return (0);
}
